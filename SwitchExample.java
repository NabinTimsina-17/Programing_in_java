public class SwitchExample {
    public static void main(String[] args) {
        int number=2;
        switch(number){

            case 1: System.out.println("10");
                break;
            case 2: System.out.println("20");
                break;
            case 3: System.out.println("30");
                break;
            default:System.out.println("Not in 1, 2 or 3");
        }
    }
}